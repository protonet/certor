describe("DepotsCreators", () => {
  it("check", async () => {
    let uuid = Uuid.v4().toString();
    let df = new DepotsCreators()
    let wc = config.Certor.create(param([], uuid))
    let etcd = await wc.etcd();
    // console.log(uuid)
    assert.deepEqual([], (await df.start(param(['list'], uuid), etcd)).ok.asJson(), "get without domain empty")
    assert.deepEqual(true, (await df.start(param(['add'], uuid), etcd)).isErr(), "add without domain()")
    assert.deepEqual(true, (await df.start(param(['add', '@pem1'], uuid), etcd)).isErr(), "add without domain()")

    let pem1_fname = `.pem1.test.${uuid}`
    fs.writeFileSync(pem1_fname, "pem-kaputt")
    assert.deepEqual(true, (await df.start(param(['add', `@${pem1_fname}`], uuid), etcd)).isErr(), "add without domain()")
    fs.unlinkSync(pem1_fname)

    for (let i = 0; i < 2; ++i) {
      let pem2_fname = `.pem2.test.${uuid}`
      fs.writeFileSync(pem2_fname, validCert());
      assert.deepEqual(["96bcec06264976f37460779acf28c5a7cfe8a3c0aae11a8ffcee05c0bddf08c6.ISRGRootX1"], 
        (await df.start(param(['add', `@${pem2_fname}`], uuid), etcd)).ok.asJson(), "add without domain()")
      fs.unlinkSync(pem2_fname)
    }
    let pem3_fname = `.pem3.test.${uuid}`
    fs.writeFileSync(pem3_fname, validCert2());
    assert.deepEqual(["96bcec06264976f37460779acf28c5a7cfe8a3c0aae11a8ffcee05c0bddf08c6.ISRGRootX1",
      "9acfab7e43c8d880d06b262a94deeee4b4659989c3d0caf19baf6405e41ab7df.VeriSignClass3PublicPrimaryCertificationAuthority-G5"], 
      (await df.start(param(['add', `@${pem3_fname}`], uuid), etcd)).ok.asJson().sort(), "add without domain()")
    fs.unlinkSync(pem3_fname)

    assert.deepEqual([
      "96bcec06264976f37460779acf28c5a7cfe8a3c0aae11a8ffcee05c0bddf08c6.ISRGRootX1",
      "9acfab7e43c8d880d06b262a94deeee4b4659989c3d0caf19baf6405e41ab7df.VeriSignClass3PublicPrimaryCertificationAuthority-G5"
    ], (await df.start(param(['list'], uuid), etcd)).ok.asJson().sort(), "get without domain empty")

    assert.deepEqual(true, (await df.start(param(['del', '@pem1'], uuid), etcd)).isErr(), "add without domain()")
    assert.deepEqual(true, (await df.start(param(['del', 'pem1'], uuid), etcd)).isErr(), "add without domain()")
    assert.deepEqual(["9acfab7e43c8d880d06b262a94deeee4b4659989c3d0caf19baf6405e41ab7df.VeriSignClass3PublicPrimaryCertificationAuthority-G5"], 
      (await df.start(param(['del', '96bcec06264976f37460779acf28c5a7cfe8a3c0aae11a8ffcee05c0bddf08c6.ISRGRootX1'], uuid), etcd)).ok.asJson(), "add without domain()")
    fs.writeFileSync(pem3_fname, validCert2());
    assert.deepEqual([], (await df.start(param(['del', `@${pem3_fname}`], uuid), etcd)).ok.asJson(), "add without domain()")
    fs.unlinkSync(pem3_fname)

    assert.deepEqual([], (await df.start(param(['list'], uuid), etcd)).ok.asJson(), "get without domain empty")
  })
})

